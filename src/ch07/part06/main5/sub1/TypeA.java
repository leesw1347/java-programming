package ch07.part06.main5.sub1;

public class TypeA {
    /**
     * private, public, protected, (default) 전역변수 정의
     */

    /**
     * private 전역변수 정의
     */
    private String name = "테스트";
    private int value = 1;

    /**
     * public 전역변수 정의
     */
    public String name2 = "테스트2";
    public int value2 = 2;

    /**
     * protected 전역변수 정의
     */
    protected int value3 = 3;

    /**
     * default 전역변수 정의
     */
    int value4 = 4;
}